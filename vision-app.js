// Desconto real a partir do preço cheio (compareAt) já presente no catálogo.
function visionDiscountPct(price, compareAt) {
  const p = Number(price) || 0;
  const c = Number(compareAt) || 0;
  if (!c || c <= p) return 0;
  return Math.round((1 - p / c) * 100);
}

function visionDiscountPill(price, compareAt) {
  const pct = visionDiscountPct(price, compareAt);
  return pct ? `<span class="vsn-off">-${pct}%</span>` : "";
}

function visionWasPrice(price, compareAt, currency) {
  const pct = visionDiscountPct(price, compareAt);
  return pct ? `<span class="vsn-was">${escapeHtml(formatMoney(compareAt, currency))}</span>` : "";
}

// Escassez determinística (sem random): itens mais caros/raros mostram menos estoque.
function visionScarcity(price) {
  const p = Number(price) || 0;
  if (p >= 1500) return { n: 1, low: true };
  if (p >= 400) return { n: 2, low: true };
  if (p >= 150) return { n: (Math.floor(p) % 3) + 2, low: false };
  return { n: (Math.floor(p) % 5) + 4, low: false };
}

function visionScarcityBadge(price, available) {
  if (available === false) return "";
  const s = visionScarcity(price);
  const text = s.n === 1 ? "Última unidade" : `${s.n} disponíveis`;
  return `<span class="vsn-stock${s.low ? " low" : ""}"><i></i>${text}</span>`;
}

