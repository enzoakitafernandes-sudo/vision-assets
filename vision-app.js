const CENTRAL_CART_ALLOWED_HOST = "visioninc.centralcart.ai";

// Seguranca: so aceita URLs HTTPS do dominio oficial da CentralCart.
// Qualquer outra URL (http://, outro dominio, valor invalido) e descartada.
function sanitizeCentralCartUrl(url) {
  if (!url) return "";
  try {
    const target = new URL(url, CENTRAL_CART_BASE_URL);
    if (target.protocol !== "https:") return "";
    if (target.hostname !== CENTRAL_CART_ALLOWED_HOST) return "";
    return target.href;
  } catch (error) {
    return "";
  }
}

// Seguranca: navegacao interna so para paginas do proprio site (mesma origem).
function sanitizeInternalUrl(url) {
  if (!url) return "";
  try {
    const target = new URL(url, window.location.href);
    if (target.origin !== window.location.origin) return "";
    return target.href;
  } catch (error) {
    return "";
  }
}

function centralCartUrl(path) {
  if (!path) return `${CENTRAL_CART_BASE_URL}/`;
  if (/^https?:\/\//i.test(path)) return sanitizeCentralCartUrl(path);
  return `${CENTRAL_CART_BASE_URL}${path.startsWith("/") ? "" : "/"}${path}`;
}

