function initVisionCategories() {
  const cards = document.querySelectorAll("[data-vision-category]");
  const products = document.querySelector("[data-vision-products]");
  if (cards.length === 0 || !products) return;

  cards.forEach((card) => {
    const key = card.dataset.visionCategory;
    card.addEventListener("mouseenter", () => renderVisionCategory(key));
    card.addEventListener("focus", () => renderVisionCategory(key));
    card.addEventListener("click", () => openCategoryPage(key));
    card.addEventListener("keydown", (event) => {
      if (event.key !== "Enter" && event.key !== " ") return;
      event.preventDefault();
      openCategoryPage(key);
    });
  });

  products.addEventListener("click", (event) => {
    const button = event.target.closest("[data-vision-add]");
    const card = event.target.closest(".vision-product-card");
    if (!button && !card) return;
    event.preventDefault();
    const targetCard = card || button.closest(".vision-product-card");
    if (button) {
      const product = cardCartProduct(targetCard, "fortnite");
      if (!product) return;
      visionAddToCart(product);
      flashAddedFeedback(button);
      return;
    }
    redirectToCentralCart(getCardCheckoutUrl(targetCard, "fortnite"));
  });

  const initialCategory = normalizeCategory(new URLSearchParams(window.location.search).get("category"));
  renderVisionCategory(initialCategory);
}

