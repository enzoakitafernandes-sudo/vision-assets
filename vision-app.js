// Sugestões de compra: prioriza a mesma família/categoria do último item,
// faixa de preço próxima e complementos baratos de outra família.
function buildCartSuggestions(limit = 3) {
  const catalog = slugCatalog();
  const inCart = new Set(state.cart.map((item) => item.slug));
  const last = state.cart.length ? state.cart[state.cart.length - 1] : null;
  const lastInfo = last ? catalog[last.slug] : null;

  const scored = Object.keys(catalog)
    .filter((slug) => !inCart.has(slug) && catalog[slug].image && Number.isInteger(VISION_PACKAGE_IDS[slug]))
    .map((slug) => {
      const info = catalog[slug];
      let score = 0;
      if (lastInfo && last) {
        if (info.family === lastInfo.family) score += 2;
        if (info.group && info.group === lastInfo.group) score += 2;
        const ratio = last.price > 0 ? info.price / last.price : 1;
        if (ratio >= 0.4 && ratio <= 1.8) score += 1.5;
        else if (ratio > 1.8 && ratio <= 3.5) score += 1;
        if (info.family !== lastInfo.family && info.price > 0 && info.price <= 120) score += 0.75;
      } else if (info.price >= 30 && info.price <= 200) {
        score += 1;
      }
      return { slug, info, score };
    })
    .sort((a, b) => b.score - a.score || a.info.price - b.info.price);

  return scored.slice(0, limit);
}

