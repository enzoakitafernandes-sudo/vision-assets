function platformCartImport() {
  let entries = null;
  try {
    const raw = localStorage.getItem(PLATFORM_CART_KEY);
    entries = raw === null ? null : JSON.parse(raw);
  } catch (error) {
    entries = null;
  }
  if (entries === null) {
    state.cartForeign = [];
    return;
  }
  if (!Array.isArray(entries)) entries = [];

  const idToSlug = {};
  Object.keys(VISION_PACKAGE_IDS).forEach((slug) => {
    idToSlug[VISION_PACKAGE_IDS[slug]] = slug;
  });

  const nextCart = [];
  const nextForeign = [];
  entries.forEach((entry) => {
    const id = parseInt(entry && entry.id, 10);
    if (!Number.isInteger(id)) return;
    const qty = Math.min(99, Math.max(1, parseInt(entry.quantity, 10) || 1));
    const slug = idToSlug[id];
    if (!slug) {
      nextForeign.push({ id, quantity: qty, fields: entry.fields || {} });
      return;
    }
    const known = state.cart.find((item) => item.slug === slug);
    if (known) {
      known.qty = qty;
      nextCart.push(known);
      return;
    }
    const info = catalogInfoForSlug(slug);
    nextCart.push({ slug, name: info.name, price: info.price, currency: info.currency, image: info.image, qty });
  });
  state.cart = nextCart;
  state.cartForeign = nextForeign;
}

